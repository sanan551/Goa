function borderRadius() {
    let image = document.getElementById("poster");
    image.style.borderRadius = "15px";
}

function replacePhoto() {
    let image = document.getElementById("poster");
    image.src = "image.jpeg";
}

function boxShadow() {
    let image = document.getElementById("poster");
    image.style.boxShadow = "10px 10px 5px #888888";
}
